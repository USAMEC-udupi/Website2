// Helper function to get image by key
export const getImage = (section, key) => {
  if (images[section] && images[section][key]) {
    return images[section][key];
  }
  console.warn(`Image not found: ${section}.${key}`);
  return images.placeholders.signature; // fallback
};

// Helper function to get all images from a section
export const getSectionImages = (section) => {
  return images[section] || {};
};

// Helper function to update an image URL
export const updateImage = (section, key, newUrl) => {
  if (images[section] && images[section][key]) {
    images[section][key] = newUrl;
    console.log(`Updated image: ${section}.${key} = ${newUrl}`);
  } else {
    console.warn(`Cannot update image: ${section}.${key} not found`);
  }
};

